func TestProjectAnalyzeReportsIndex(t *testing.T) {
	root := fixture(t, map[string]string{
		"internal/handler/identity_user.handler.go": `package handler
import (
	"net/http"
	"github.com/danielgtaylor/huma/v2"
)
type identityUserHandler struct{}
func RegisterIdentityUsers(api huma.API) {
	users := huma.NewGroup(api, "/identity/users")
	huma.Register(users, huma.Operation{OperationID: "identity-user-list", Method: http.MethodGet, Path: "", Tags: []string{"Identity"}}, identityUserHandler{}.List)
}
func (h identityUserHandler) List() {}
`,
		"internal/service/identity_user.service.go": `package service
type AuthSessionService struct{}
func NewAuthSessionService() *AuthSessionService { return &AuthSessionService{} }
type IdentityUserService struct { sessions *AuthSessionService }
func NewIdentityUserService() *IdentityUserService { return &IdentityUserService{} }
func (s *IdentityUserService) Create() {}
func (s *IdentityUserService) Search() {}
`,
		"internal/repository/identity_user.store.go": `package repository
import "context"
func (s *Store) FetchIdentityUser(ctx context.Context, id int64) (string, error) { return "", nil }
func (s *Store) ListIdentityUsers(ctx context.Context) ([]string, error) { return nil, nil }
`,
		"internal/repository/identity_user.schema.go": `package repository
import (
	"context"
	"time"
	"github.com/uptrace/bun"
)
type IdentityUserModel struct {
	bun.BaseModel ` + "`" + `bun:"table:users,alias:u"` + "`" + `
	ID int64 ` + "`" + `bun:"id,pk,autoincrement"` + "`" + `
	Username string ` + "`" + `bun:"username,notnull,unique"` + "`" + `
	Email string ` + "`" + `bun:"email,nullzero"` + "`" + `
	CreatedAt time.Time ` + "`" + `bun:"created_at,notnull"` + "`" + `
}
func (*IdentityUserModel) BeforeCreateTable(_ context.Context, query *bun.CreateTableQuery) error {
	query.ForeignKey("(id) REFERENCES accounts (user_id) ON DELETE CASCADE")
	return nil
}
func IdentityUserSchema() {}
`,
	})

	analysis, err := (Project{Root: filepath.Join(root, "internal")}).Analyze()
	if err != nil {
		t.Fatal(err)
	}
	index := analysis.ProjectIndex()

	if index.ModulePath != "example.com/app" {
		t.Fatalf("unexpected module path %q", index.ModulePath)
	}
	assertAPIIndex(t, index, "GET", "/identity/users", "identity-user-list", "List", []string{"Identity"})
	assertHandlerIndex(t, index, "identity_user", "identityUserHandler", "RegisterIdentityUsers", []string{"List"})
	assertServiceIndex(t, index, "IdentityUserService", "identity_user", "NewIdentityUserService", []string{"AuthSessionService"}, []string{"Create", "Search"})
	assertStoreIndex(t, index, "identity_user", []string{"FetchIdentityUser", "ListIdentityUsers"})

	users := findTableIndex(index, "users")
	if users == nil {
		t.Fatalf("expected users table, got %#v", index.Tables)
	}
	if users.Model != "IdentityUserModel" || users.Alias != "u" || users.Scope != "identity_user" {
		t.Fatalf("unexpected users table metadata: %#v", users)
	}
	assertFieldIndex(t, *users, "ID", "id", "int64", true, true, false, false, "")
	assertFieldIndex(t, *users, "Username", "username", "string", false, false, true, false, "true")
	assertFieldIndex(t, *users, "Email", "email", "string", false, false, false, true, "")
	if len(users.ForeignKeys) != 1 || users.ForeignKeys[0] != "(id) REFERENCES accounts (user_id) ON DELETE CASCADE" {
		t.Fatalf("unexpected foreign keys: %#v", users.ForeignKeys)
	}
}

func TestIndexTextIncludesSections(t *testing.T) {
	index := indexFixture()

	text := index.Text()
	for _, needle := range []string{
		"tddcheck index: example.com/app",
		"apis:",
		"- GET /devices (device) internal/handler/device.handler.go",
		"operation: list-devices",
		"handlers:",
		"- deviceHandler (device) internal/handler/device.handler.go",
		"services:",
		"- DeviceService (device) internal/service/device.service.go",
		"dependencies: AuthSessionService",
		"stores:",
		"- device internal/repository/device.store.go",
		"tables:",
		"- devices (DeviceModel, device) internal/repository/device.schema.go",
		"ID int64 `id` [pk]",
	} {
		if !strings.Contains(text, needle) {
			t.Fatalf("expected text to contain %q, got:\n%s", needle, text)
		}
	}
}

