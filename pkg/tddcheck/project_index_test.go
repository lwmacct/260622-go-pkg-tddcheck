func assertAPIIndex(t *testing.T, index Index, method string, path string, operationID string, handler string, tags []string) {
	t.Helper()

	for _, api := range index.APIs {
		if api.Method != method || api.FullPath != path || api.OperationID != operationID {
			continue
		}
		if api.Handler != handler {
			t.Fatalf("unexpected api handler for %s %s: %#v", method, path, api)
		}
		for _, tag := range tags {
			if !stringIn(tag, api.Tags) {
				t.Fatalf("expected tag %s in %#v", tag, api.Tags)
			}
		}
		return
	}
	t.Fatalf("expected api %s %s %s, got %#v", method, path, operationID, index.APIs)
}

func assertHandlerIndex(t *testing.T, index Index, scope string, handlerType string, register string, methods []string) {
	t.Helper()

	for _, handler := range index.Handlers {
		if handler.Scope != scope {
			continue
		}
		if handler.Type != handlerType || !stringIn(register, handler.Registers) {
			t.Fatalf("unexpected handler metadata for %s: %#v", scope, handler)
		}
		assertMethods(t, handler.Methods, methods)
		return
	}
	t.Fatalf("expected handler %s, got %#v", scope, index.Handlers)
}

func assertServiceIndex(t *testing.T, index Index, serviceType string, scope string, constructor string, dependencies []string, methods []string) {
	t.Helper()

	for _, service := range index.Services {
		if service.Type != serviceType {
			continue
		}
		if service.Scope != scope || service.Constructor != constructor {
			t.Fatalf("unexpected service metadata for %s: %#v", serviceType, service)
		}
		for _, dependency := range dependencies {
			if !stringIn(dependency, service.Dependencies) {
				t.Fatalf("expected dependency %s in %#v", dependency, service.Dependencies)
			}
		}
		assertMethods(t, service.Methods, methods)
		return
	}
	t.Fatalf("expected service %s, got %#v", serviceType, index.Services)
}

func assertStoreIndex(t *testing.T, index Index, scope string, methods []string) {
	t.Helper()

	for _, store := range index.Stores {
		if store.Scope != scope {
			continue
		}
		assertMethods(t, store.Methods, methods)
		return
	}
	t.Fatalf("expected store %s, got %#v", scope, index.Stores)
}

func assertMethods(t *testing.T, values []MethodIndex, methods []string) {
	t.Helper()

	for _, method := range methods {
		found := false
		for _, item := range values {
			if item.Name == method {
				found = true
				break
			}
		}
		if !found {
			t.Fatalf("expected method %s, got %#v", method, values)
		}
	}
}

func findTableIndex(index Index, table string) *TableIndex {
	for itemIndex := range index.Tables {
		if index.Tables[itemIndex].Table == table {
			return &index.Tables[itemIndex]
		}
	}
	return nil
}

func findAPIIndex(index Index, operationID string) *APIIndex {
	for itemIndex := range index.APIs {
		if index.APIs[itemIndex].OperationID == operationID {
			return &index.APIs[itemIndex]
		}
	}
	return nil
}

func assertFieldIndex(t *testing.T, table TableIndex, name string, column string, goType string, primaryKey bool, autoIncrement bool, notNull bool, nullable bool, unique string) {
	t.Helper()

	for _, field := range table.Fields {
		if field.Name != name {
			continue
		}
		if field.Column != column ||
			field.GoType != goType ||
			field.PrimaryKey != primaryKey ||
			field.AutoIncrement != autoIncrement ||
			field.NotNull != notNull ||
			field.Nullable != nullable ||
			field.Unique != unique {
			t.Fatalf("unexpected field metadata for %s.%s: %#v", table.Table, name, field)
		}
		return
	}
	t.Fatalf("expected field %s in table %s, got %#v", name, table.Table, table.Fields)
}

func stringIn(value string, values []string) bool {
	for _, item := range values {
		if item == value {
			return true
		}
	}
	return false
}
