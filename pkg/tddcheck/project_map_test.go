func assertServiceMap(t *testing.T, projectMap ProjectMap, name string, scope string, constructor string, methods []string) {
	t.Helper()

	for _, service := range projectMap.Services {
		if service.Name != name {
			continue
		}
		if service.Scope != scope || service.Constructor != constructor {
			t.Fatalf("unexpected service metadata for %s: %#v", name, service)
		}
		for _, method := range methods {
			found := false
			for _, item := range service.Methods {
				if item.Name == method {
					found = true
					break
				}
			}
			if !found {
				t.Fatalf("expected service %s to include method %s, got %#v", name, method, service.Methods)
			}
		}
		return
	}
	t.Fatalf("expected service %s, got %#v", name, projectMap.Services)
}

func findTableMap(projectMap ProjectMap, table string) *TableMap {
	for index := range projectMap.Tables {
		if projectMap.Tables[index].Table == table {
			return &projectMap.Tables[index]
		}
	}
	return nil
}

func assertFieldMap(t *testing.T, table TableMap, name string, column string, goType string, primaryKey bool, autoIncrement bool, notNull bool, nullable bool, unique string) {
	t.Helper()

	for _, field := range table.Fields {
		if field.Name != name {
			continue
		}
		if field.Column != column ||
			field.GoType != goType ||
			field.PrimaryKey != primaryKey ||
			field.AutoIncrement != autoIncrement ||
			field.NotNull != notNull ||
			field.Nullable != nullable ||
			field.Unique != unique {
			t.Fatalf("unexpected field metadata for %s.%s: %#v", table.Table, name, field)
		}
		return
	}
	t.Fatalf("expected field %s in table %s, got %#v", name, table.Table, table.Fields)
}
